/* 
 * bitXor - x^y using only ~ and & 
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14
 *   Rating: 1
 */
int bitXor(int x, int y) {
       //x^y = (x|y) & ~(x&y)
       //x|y = ~(~x & ~y)
       //then replace x|y in first line with second line
       return (~(~x&~y) & ~(x&y));
}
/* 
 * upperBits - pads n upper bits with 1's
 *  You may assume 0 <= n <= 32
 *  Example: upperBits(4) = 0xF0000000
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 10
 *  Rating: 1
 */
int upperBits(int n) {
  //!!n&1 takes care of the n=0 case
  //shifting it to the left 31 times brings the 0th bit to the 31st position
  //shifting it back n-1 times gives n 1's and the rest of the original number 
  return ((!!n&1) << 31) >> (n + ~0);
}
