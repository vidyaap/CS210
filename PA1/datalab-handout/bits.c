/* 
 * divpwr2 - Compute x/(2^n), for 0 <= n <= 30
 *  Round toward zero
 *   Examples: divpwr2(15,1) = 7, divpwr2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int divpwr2(int x, int n) {
  //is_neg determines whether x is negative by and-ing the sign bit with all 1s
  //then and the negative case with negative x's and vice versa
  //or the two cases
  int is_neg = ((!!(x&(1<<31)))<<31)>>31;
  int neg_case = (x+(1<<n)+(~0));
  return (((~(is_neg)) & x) | 
    ((is_neg) & neg_case))>>n;
  
}
/* 
 * leastBitPos - return a mask that marks the position of the
 *               least significant 1 bit. If x == 0, return 0
 *   Example: leastBitPos(96) = 0x20
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2 
 */
int leastBitPos(int x) {
  //negative x is x with all the opposite sign bit in front
  //(negative x) plus 1 switches the bits and shifts them left once
  //and-ing it and x isolates the least significant 1
  int neg_x = (~x)+1;
  return x & (neg_x);
}
/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
  //right shifting x by n-1 leaves the nth bit in the first spot
  //the nth bit plus one is guaranteed to have a one in it so "not"
  //that will always be false and not the nth bit will only be true
  //if x fits in n bits
  int nth_bit = x>>(n+(~0));
  return (!(nth_bit+1)) | (!nth_bit);
}
/* 
 * anyOddBit - return 1 if any odd-numbered bit in word set to 1
 *   Examples anyOddBit(0x5) = 0, anyOddBit(0x7) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 2
 */
int anyOddBit(int x) {
  //the constant 0xaa has ones in odd bits but only for 8 bits
  //since an int is 32 bits, shifting it by 8 four times will allow
  //you to look at every bit and compare it to 0xaa
    int odds = 0xaa;
    int one, two, three, four, total;
    one = x&odds;
    x = x>>8;
    two = x&odds;
    x = x>>8;
    three = x&odds;
    x = x>>8;
    four = x&odds;
    total = one + two + three + four;
    return !!total;
}
