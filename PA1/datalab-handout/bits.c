/* 
 * logicalShift - shift x to the right by n, using a logical shift
 *   Can assume that 0 <= n <= 31
 *   Examples: logicalShift(0x87654321,4) = 0x08765432
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3 
 */
int logicalShift(int x, int n) {
  //first right shift x arithmetically, then change n to 32-n
  //shift the number 1 n+1 (in case n=32) times to pad that many bits with 1s
  //or-ing x and the shift and then xor-ing it with the shift gets rid of the 
  //top 1s
  int shift;
  x = x>>n;
  n = 32 + ~n;
  shift = ((~0x0)<<n)<<1;
  x = (x|shift)^shift;
  return x;
}
/*
 * satMul2 - multiplies by 2, saturating to Tmin or Tmax if overflow
 *   Examples: satMul2(0x30000000) = 0x60000000
 *             satMul2(0x40000000) = 0x7FFFFFFF (saturate to TMax)
 *             satMul2(0x60000000) = 0x80000000 (saturate to TMin)
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int satMul2(int x) {
  //represent int_min by 1<<31; then figure out whether x is positive by 
  //complementing x's sign bit; store the product and then test the cases
  //for whether x is positive and the sign is positive 
  int int_min = 1<<31;
  int is_pos = ~(x>>31);
  int prod = x<<1;
  int p_sign = (prod>>31);
  int test1 = is_pos & p_sign;
  int test2 = ~is_pos & ~p_sign;
return (test1 & (~int_min))
    |(test2 & int_min)
    |(((~test1)&(~test2))&prod);
}
/* 
 * isNonNegative - return 1 if x >= 0, return 0 otherwise 
 *   Example: isNonNegative(-1) = 0.  isNonNegative(0) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 3
 */
int isNonNegative(int x) {
  return !(x&(1<<31));
}
