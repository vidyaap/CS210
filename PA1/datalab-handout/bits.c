/* 
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */
int bang(int x) {
  //"and" the complemet of negative x and the complement of x
  //then right shift by 31 and "and" it with 1
  int neg_x = ~x + 1;
  return ((~neg_x & ~x)>> 31)&1;
}
